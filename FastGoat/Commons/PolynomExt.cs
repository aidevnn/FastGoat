/// <summary>
/// Extension class for Conway polynomial. 
/// </summary>
public static class PolynomExt
{
    /// <summary>
    /// Static constructor for the <see cref="PolynomExt"/> class. 
    /// </summary>
    static PolynomExt()
    {
        AllConwayPolys = new();
        foreach (var str in ConwayPolysTable)
        {
            var seq = Regex.Matches(str, @"\d+").Select(e => e.Value).ToArray();
            var (p, m, coefs) = (int.Parse(seq[0]), int.Parse(seq[1]), seq.Skip(2).Select(e => int.Parse(e)).ToArray());
            if (!AllConwayPolys.ContainsKey(p))
                AllConwayPolys[p] = new();

            AllConwayPolys[p][m] = coefs;
        }
    }

    /// <summary>
    /// Represents a dictionary of all Conway Polynomials.
    /// </summary>
    public static readonly Dictionary<int, Dictionary<int, int[]>> AllConwayPolys;

    /// <summary>
    /// Gets the Conway polynomial of a given integer q.
    /// </summary>
    /// <param name="q">The integer for which to calculate the Conway polynomial.</param>
    /// <returns>A tuple containing the prime and multiplicity of the polynomial, as well as an array of coefficients.</returns>
    public static ((int p, int m) pm, int[] coefs) GetConwayPoly(BigInteger q)
    {
        if (q < IntExt.MaxPrime && IntExt.Primes10000.Contains((int)q) && !AllConwayPolys.ContainsKey((int)q))
        {
            var p0 = (int)q;
            var k = IntExt.Solve_k_pow_m_equal_one_mod_n_strict(p0, (p0 - 1));
            return ((p0, 1), [IntExt.AmodP(-k, p0), 1]);
        }

        var pm = IntExt.PrimesDecompositionBigInt(q).ToArray();
        if (pm.Distinct().Count() != 1)
            throw new();

        var p = pm[0];
        var m = pm.Length;
        if (AllConwayPolys.ContainsKey(p))
        {
            var allCP = AllConwayPolys[p];
            if (allCP.ContainsKey(m))
                return ((p, m), AllConwayPolys[p][m]);
            else
                return ((p, m), Array.Empty<int>());
        }
        else
            return ((p, m), Array.Empty<int>());
    }

